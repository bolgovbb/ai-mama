async def seed():
    from sqlalchemy import text
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)
        # Add new columns if not exist
        for col in ["norm_text", "concern_text", "exercises"]:
            try:
                col_type = "TEXT" if col != "exercises" else "JSONB"
                await conn.execute(text(f"ALTER TABLE milestones ADD COLUMN IF NOT EXISTS {col} {col_type}"))
            except Exception:
                pass

    async with async_session() as db:
        for m_data in MILESTONES:
            code = m_data["code"]
            from sqlalchemy import select
            existing = (await db.execute(select(Milestone).where(Milestone.code == code))).scalar_one_or_none()
            if existing:
                existing.title = m_data["title"]
                existing.description = m_data.get("description")
                existing.norm_text = m_data.get("norm_text")
                existing.concern_text = m_data.get("concern_text")
                existing.exercises = m_data.get("exercises")
                existing.age_months_min = m_data["age_months_min"]
                existing.age_months_max = m_data["age_months_max"]
                existing.age_months_concern = m_data.get("age_months_concern")
                existing.source = m_data.get("source", "WHO")
            else:
                db.add(Milestone(
                    code=code, domain=m_data["domain"], title=m_data["title"],
                    description=m_data.get("description"),
                    age_months_min=m_data["age_months_min"], age_months_max=m_data["age_months_max"],
                    age_months_concern=m_data.get("age_months_concern"),
                    source=m_data.get("source", "WHO"),
                    norm_text=m_data.get("norm_text"),
                    concern_text=m_data.get("concern_text"),
                    exercises=m_data.get("exercises"),
                ))
        await db.commit()
        total = len(MILESTONES)
        print(f"Seeded/updated {total} milestones with exercises")

asyncio.run(seed())
